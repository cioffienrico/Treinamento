package exec2;
public class Operacoes {

	/**
	 * Classe criada para demonstrar Operacoes
	 * @author ecioffi
	 * @since 11/02/2020
	 * @version 0.1
	 */

	public static void main(String[] args) {

	//exemplo de adição
		int adic = 10 + 15;
	//exemplo de subtração 
		int sub = 15 - 10;
	//exemplo de multiplicação
		int mult = 5 * 10;
	//exemplo de divisão
		int div = 50 / 5;
	//exemplo de modulo
		int mod = 20 % 6;
		
	System.out.println("10 + 15 = " + adic);
	System.out.println("15 - 10 = " + sub);
	System.out.println("5 * 10 = " + mult);
	System.out.println("50 / 5 = " + div);
	System.out.println("20 % 6 = " + mod);
		
	
	}
}
